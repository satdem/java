package Gun16;

public class JavaForLoop1 {
    public static void main(String[] args) {
        //while loop
        int i=0;
        while (i<5){
            System.out.println("w-i = " + i);
            i++;
        }

        for(i=0;i<5;i++){
            System.out.println("f-i = " + i);
        }
    }
}
