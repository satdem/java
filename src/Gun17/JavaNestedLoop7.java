public class JavaNestedLoop7 {
    public static void main(String[] args) {
        //*****
        //****
        //***
        //**
        //*
        for(int i=5;i>0;i--){
            for(int j = 1; i >= j; j++){
                System.out.print("*");
            }
            System.out.println();
        }


    }
}
