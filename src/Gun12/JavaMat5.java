package Gun12;

public class JavaMat5 {
    public static void main(String[] args) {
        int a=-12;
        int b=2;
        //abs mutlak değer demek
        System.out.println("a nın MutlakDeğeri= "+Math.abs(a));
        //max a ve b den büyük olanı verir
        System.out.println("a ve b den büyüğü = " + Math.max(a,b));
        //min küçük olanı verir
        System.out.println("min(a,b) = " + Math.min(a,b));
        //sqrt karakök
        System.out.println("16 nın karekökü= " +Math.sqrt(16));
        //pow kuvvet alma
        System.out.println("2 nin 3. kuvveti= "+Math.pow(2,3));
        System.out.println("2 nin -3. kuvveti= "+Math.pow(2,-3));
        //round yuvarlama yapar
        System.out.println("round of 3,1= "+Math.round(3.1));//3
        System.out.println("round of 3,5= "+Math.round(3.5));//4
        //ceil tavana yuvarlar
        System.out.println("ceil of  3.1= "+Math.ceil(3.1));//4
        System.out.println("ceil of  3.9= "+Math.ceil(3.9));//4
        //floor tabana yuvarlar
        System.out.println("floor of  3.1= "+Math.floor(3.1));//3
        System.out.println("floor of  3.9= "+Math.floor(3.9));//3




    }
}
