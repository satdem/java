package Mentoring.mentorluk12zeynep;

public class NestedLoop1 {
    public static void main(String[] args) {
        for (int i=0;i<=10;i++){

            for(int j=0;j<4;j++){
                System.out.println("i = " + i+"j= "+j);
            }
            System.out.println();
        }

        //ornek2
        for(int i=0; i<10; i++){

            for(int j=0; j<10; j++){
                System.out.print("#");
            }System.out.println();
        }

    }

}
