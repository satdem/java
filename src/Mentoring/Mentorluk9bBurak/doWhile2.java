package Mentoring.Mentorluk9bBurak;

public class doWhile2 {
    public static void main(String[] args) {
        int count = 11;
        while(count < 11) {
            System.out.println("Count is: " + count);
            count++;
        }

        int doCount = 11;
        do{
            System.out.println("DoCount is: " + doCount);
            doCount++;
        } while(doCount < 11);
    }
}
