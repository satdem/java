package Gun9;

public class JavaArithmeticOperators1 {
    public static void main(String[] args) {
        int a=8;
        int b=4;
        //toplama için
        System.out.println("Toplama işlemi ");
        System.out.println("a+b =" +(a+b));
        System.out.println("5+2= "+(5+2));

        System.out.println("Çıkarma işlemi");
        System.out.println("a-b= "+(a-b));
        System.out.println("5-2= "+(5-2));

        System.out.println("negatifleştirme");
        System.out.println("b negatifleştirilirse: "+(-b));
        System.out.println("a negatifleştirilirse:"+(-a));
        a=-a;//sayının eksi hali lazımsa bu şekildede yapabiliyoruz.(iki şekildede oluyor.)
        System.out.println("a nın şu anki hali: "+a);





    }
}
