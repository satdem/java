package Gun9;

public class JavaArithmeticOperators {
    public static void main(String[] args) {
        int a=10;
        int b=5;

        System.out.println("çarpma işlemi");
        System.out.println("a*b= "+(a*b));//parantez olmayabilir burada işlem önceliği var çarpma önce oluyor zaten.

        System.out.println("Bölme işlemi");
        System.out.println("a/b= "+(a/b));

        System.out.println("Modül işlemi");
        System.out.println("a%b= "+(a%b));//% bu işaret modül demek, yüzde ile ilgisi yok.

    }
}
